#!/usr/bin/env node
/**
 * V8.64 Sprint 70 第128轮：locale chunk 构建脚本
 * Why: Global 何+前端小凡——当前 42 locale 翻译数据预期全部打包在主 bundle 里，
 *      需按需加载。本脚本从 i18n.js DICT 中提取各 locale 到独立 chunk 文件，
 *      配合 vite.config.js 动态导入实现按需加载。
 *
 * How: 读取 src/h5/utils/i18n.js → 解析 DICT 对象 → 每个 locale 输出独立 JSON chunk
 *      输出到 src/h5/locales/{locale}.json
 *      保留 i18n.js 中的 zh 作为 fallback（内联在主 bundle 中）
 *
 * 架构: 当前仅 zh/en 2 个 locale，预留 42 locale 扩展能力。
 *       全量落地 Sprint 70 验证。
 *
 * Usage: node scripts/build-locale-chunks.cjs
 */
const fs = require('fs');
const path = require('path');

const I18N_SRC = path.resolve(__dirname, '..', 'src', 'h5', 'utils', 'i18n.js');
const LOCALES_OUT = path.resolve(__dirname, '..', 'src', 'h5', 'locales');
const LOCALES_INDEX = path.join(LOCALES_OUT, 'index.js');

// 需要从主 bundle 中排除的 locale（zh 保留为 fallback 内联）
const CHUNK_LOCALES = ['en']; // 未来扩展到 42 locale 时追加

function extractDICT(source) {
  // 从 i18n.js 中提取 DICT 对象
  const dictStart = source.indexOf('export const DICT = {');
  if (dictStart === -1) {
    console.error('[build-locale-chunks] ERROR: Cannot find DICT export in i18n.js');
    process.exit(1);
  }

  // 从 DICT 开始位置提取到文件末尾（找到对应的顶层 };
  const dictBody = source.substring(dictStart + 'export const DICT = '.length);

  // 匹配每个 locale 块：  zh: { ... },  或  'en-GB': {},
  // 使用括号匹配提取每个 locale 的完整对象
  const locales = {};
  const localeRegex = /^\s{2}(\w+|'[\w-]+'):\s*\{/gm;
  let match;
  const positions = [];

  while ((match = localeRegex.exec(dictBody)) !== null) {
    let name = match[1];
    // 去掉引号
    if (name.startsWith("'") && name.endsWith("'")) {
      name = name.slice(1, -1);
    }
    positions.push({ name, start: match.index });
  }

  for (let i = 0; i < positions.length; i++) {
    const { name } = positions[i];
    const startIdx = positions[i].start;
    // 找到第一个 {
    const braceIdx = dictBody.indexOf('{', startIdx);
    if (braceIdx === -1) continue;

    // 括号匹配找到对应的 }
    let depth = 0;
    let endIdx = braceIdx;
    for (let j = braceIdx; j < dictBody.length; j++) {
      if (dictBody[j] === '{') depth++;
      if (dictBody[j] === '}') {
        depth--;
        if (depth === 0) {
          endIdx = j;
          break;
        }
      }
    }

    const block = dictBody.substring(startIdx, endIdx + 1);
    // 提取 key: value 部分
    const colonIdx = block.indexOf(':');
    const valueStr = block.substring(colonIdx + 1).trim();

    try {
      // 用 Function 安全求值
      const fn = new Function(`return ${valueStr}`);
      locales[name] = fn();
    } catch (e) {
      console.error(`[build-locale-chunks] ERROR parsing locale "${name}":`, e.message);
      // 尝试解析空对象
      if (valueStr === '{}') {
        locales[name] = {};
      }
    }
  }

  return locales;
}

function main() {
  console.log('[build-locale-chunks] Reading i18n.js...');
  const source = fs.readFileSync(I18N_SRC, 'utf-8');
  const locales = extractDICT(source);

  console.log(`[build-locale-chunks] Extracted ${Object.keys(locales).length} locales:`, Object.keys(locales).join(', '));

  // 确保输出目录存在
  if (!fs.existsSync(LOCALES_OUT)) {
    fs.mkdirSync(LOCALES_OUT, { recursive: true });
  }

  // 为每个 chunk locale 生成 JSON 文件
  const chunkFiles = [];
  for (const locale of CHUNK_LOCALES) {
    if (!locales[locale]) {
      console.warn(`[build-locale-chunks] WARNING: locale "${locale}" not found in DICT, skipping`);
      continue;
    }
    const outPath = path.join(LOCALES_OUT, `${locale}.json`);
    fs.writeFileSync(outPath, JSON.stringify(locales[locale], null, 2), 'utf-8');
    chunkFiles.push(locale);
    console.log(`[build-locale-chunks] Written: ${outPath} (${Object.keys(locales[locale]).length} keys)`);
  }

  // 生成 index.js 动态导入入口
  const indexContent = `// Auto-generated by scripts/build-locale-chunks.cjs — DO NOT EDIT
// V8.64 Sprint 70 第128轮：locale chunk 按需加载入口
// Why: Global 何+前端小凡——42 locale 翻译数据不打包在主 bundle 中，
//      仅 zh fallback 内联，其他 locale 通过动态 import() 按需加载。

const CHUNK_MAP = {
${chunkFiles.map(l => `  '${l}': () => import('./${l}.json'),`).join('\n')}
};

/** 已加载的 locale chunk 缓存 */
const _loaded = {};

/**
 * 按需加载指定 locale 的翻译数据
 * @param {string} locale - locale 代码，如 'en'
 * @returns {Promise<object>} 翻译字典对象
 */
export async function loadLocaleChunk(locale) {
  const base = (locale || '').toLowerCase().split('-')[0];
  if (!CHUNK_MAP[base]) return null;
  if (_loaded[base]) return _loaded[base];

  try {
    const mod = await CHUNK_MAP[base]();
    _loaded[base] = mod.default || mod;
    return _loaded[base];
  } catch (e) {
    console.warn('[i18n] Failed to load locale chunk:', base, e);
    return null;
  }
}

/**
 * 同步检查 locale chunk 是否已加载
 * @param {string} locale
 * @returns {boolean}
 */
export function isLocaleLoaded(locale) {
  const base = (locale || '').toLowerCase().split('-')[0];
  return !!_loaded[base];
}

/**
 * 获取已加载的 locale chunk（不触发加载）
 * @param {string} locale
 * @returns {object|null}
 */
export function getLoadedLocale(locale) {
  const base = (locale || '').toLowerCase().split('-')[0];
  return _loaded[base] || null;
}
`;

  fs.writeFileSync(LOCALES_INDEX, indexContent, 'utf-8');
  console.log(`[build-locale-chunks] Written: ${LOCALES_INDEX}`);
  console.log(`[build-locale-chunks] Done. ${chunkFiles.length} locale chunks generated.`);
}

main();